    private void btn_sighupActionPerformed(java.awt.event.ActionEvent evt) {                                           
       if(!CheckBlank()){// 빈칸확인
           showDialog("Error","빈칸을 모두 채워주세요");
           return;
       }
       else if(!CheckPasswd()){ // 비밀번호 일치여부 확인
           passwd1.setText("");
           passwd2.setText("");
           showDialog("Error", "비밀번호가 일치하지 않습니다.");
           return;
       }
       
       String id = this.id.getText();
       String passwd1 = String.valueOf(this.passwd1.getPassword());
       String passwd2 = String.valueOf(this.passwd2.getPassword());
       String name = this.name.getText();
       int grade = combo_grade.getSelectedIndex()+1; // index + 1 = 학년
       String dpt = (String) combo_dpt.getSelectedItem();
       int security = -1; // 학생0, 관리자1
       if(radio_manager.isSelected()){ // 학생0, 관리자1
           security = 1; // 관리자
       }
       else if(radio_student.isSelected()){
           security = 0; // 학생
       }
       String missNum = this.MissNum.getText();
       
       // 서버로 보낼 JSON데이터
       JSONObject data = new JSONObject();
       data.put("command", "register");
       data.put("id", id);
       data.put("passwd", passwd1);
       data.put("name", name);
       data.put("missNum", missNum);
       data.put("dpt", dpt);
       data.put("grade", grade);
       data.put("grade", grade);
       data.put("security", security);
       
       // 서버요청 및 응답
       JSONObject result = new ServerConnection().request(data);
     
       boolean res = result.getBoolean("result");
       String fromMessage = result.getString("message");
       
       if(res) {
           showDialog("Success", "회원가입에 성공하였습니다.");
           this.dispose(); // 회원가입 후 창닫기
       }
       else{
           showDialog("Error", fromMessage);
       }      
       
    }                                          
    
