    public void showDialog(String info, String msg){ // 서버에서 받은 Message Modal Dialog 출력
        Dialog.setTitle(info); // title지정
        this.msg.setText(msg); //메세지 지정
        Dialog.pack();
        Dialog.setLocationRelativeTo(null);
        Dialog.setVisible(true);
    }
    
    public boolean CheckBlank() { // 빈칸확인
        if(id.getText().equals("") || String.valueOf(passwd1.getPassword()).equals("") || String.valueOf(passwd2.getPassword()).equals("") || name.getText().equals("") || MissNum.getText().equals("")){
            return false;
        }
        return true;
    }
    
    
    public boolean CheckPasswd(){ // passwd 일치여부확인
        String passwd1 = String.valueOf(this.passwd1.getPassword());
        String passwd2 = String.valueOf(this.passwd2.getPassword());
        return passwd1.equals(passwd2) ? true : false;
    }
